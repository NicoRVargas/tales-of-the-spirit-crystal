def print_world_menu(characters):
    print("\n" * 50)

    print(75 * "=-")
    print(f"{'Mundo 1':^150}")
    print(75 * "=-")

    for i in range(len(characters)):
        print(f"[{i}]\t", characters[i])

    print(150 * "-")
    print(f"[1] Andar{22 * ' '}[2] Party{22 * ' '}[3]Acampar")
    print(150 * "-")

